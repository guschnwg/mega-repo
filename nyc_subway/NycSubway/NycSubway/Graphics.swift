func text(
    canvas: inout [[Color]],
    x: Int,
    y: Int,
    text: String,
    color: Color,
    from: [AnyHashable : [[Int]]],
    cropAfter: Int = 999999
) {
    var _x = x
    
    text.forEach { char in
        let font = from[String(char)]!
        
        for r in font.indices {
            for c in font[r].indices {
                if (_x + c > cropAfter) {
                    break;
                }
                
                if font[r][c] == 1 {
                    canvas[y + r][_x + c] = color
                }
            }
        }
        _x += font[0].count + 1
    }
}

func rightAlignText(
    canvas: inout [[Color]],
    x: Int,
    y: Int,
    text: String,
    color: Color,
    from: [AnyHashable : [[Int]]],
    cropAfter: Int = 999999
) {
    var _x = x
    
    text.reversed().forEach { char in
        let font = from[String(char)]!
        
        for r in font.indices {
            for c in font[r].indices.reversed() {
                if (_x - (font[0].count - c) < x - cropAfter) {
                    break;
                }
                
                if font[r][c] == 1 {
                    canvas[y + r][_x - (font[0].count - c)] = color
                }
            }
        }
        _x -= font[0].count + 1
    }
}

